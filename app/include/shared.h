#pragma once

/* Bootloader communication */
struct bootloader_app_shared_t {
    union {
        uint64_t ull;
        uint32_t ul[2];
    } crc;
    uint32_t signature;
    uint32_t bus_speed;
    uint32_t node_id;
} __attribute__ ((packed));


#define BOOTLOADER_COMMON_APP_SIGNATURE         0xB0A04150u
#define BOOTLOADER_COMMON_BOOTLOADER_SIGNATURE  0xB0A0424Cu


bool bootloader_read(bootloader_app_shared_t *shared);
void bootloader_write(const bootloader_app_shared_t *shared);
